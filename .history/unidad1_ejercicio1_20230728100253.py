"""Programa que toma tres valores por consola, luego multiplica
el 1ro por el 2do y suma el 3ro. Presenta el resultado en la terminal."""
VARIABLE1 = int(input("ingresar 1er valor: "))
VARIABLE2 = int(input("ingresar 2do valor: "))
VARIABLE3 = int(input("ingresar 3er valor: "))
print(f"El primer valor es {VARIABLE1}")
print(f"El segundo valor es {VARIABLE2}")
print(f"El tercer valor es {VARIABLE3}")
print(f"El resultado es {VARIABLE1 * VARIABLE2 + VARIABLE3}")
